#!/usr/bin/env python3
"""1. Basic annotations - concat"""
import typing


def concat(str1: str, str2: str) -> str:
    """
      concat - function that concatenate two strings

      Args:
        str1 (str): the 1st string
        str2 (str): the 2nd string

      Returns:
        new string
    """
    return f"{str1}{str2}"
